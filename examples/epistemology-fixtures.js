function run(text, expected) {
  const r = inspectE(text);
  const caught = r.passes === false;
  let correct;
  if (expected === 'pass') correct = !caught;
  else if (expected === 'catch') correct = caught;
  else correct = null;
  return { caught, correct, r };
}

